export default function Experiences() {


    const [experiences, setExperiences] = useState([])

    const getExperiences = async () => {
        try {
            const data = await axios.get(BASE_URL + "/api/v1/experience/all", {
                withCredentials: true
            })

            setExperiences(prev => data.data.data)
        }
        catch (err) {
            console.log(err)
        }
    }

    console.log("All experiences", experiences)


    useEffect(() => {
        getExperiences()
    }, [])

    if (experiences.length == 0) {
        return <Loader />
    }

    return (
        <div className="mx-6 md:mx-12 lg:mx-26 mt-20 min-h-screen mb-20">
            <div className="md:w-[50%] mt-4 mb-6">
                <SearchBar />
            </div>

            <ul role="list" className="divide-y  lg:pr-18 divide-gray-100">
                {
                    experiences.map((experience) => (
                        <Link key={experience._id} to={`/experiences/${experience._id}`}>
                            <SingleExperience experience={experience} />
                        </Link>
                    ))
                }
            </ul>


        </div>
    )
}
